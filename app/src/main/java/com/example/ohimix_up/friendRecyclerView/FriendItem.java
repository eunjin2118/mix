package com.example.ohimix_up.friendRecyclerView;

public class FriendItem {
    String word1;
    String word1Mean;

    public FriendItem(String word1, String word1Mean) {
        this.word1 = word1;
        this.word1Mean = word1Mean;
    }

    public String getWord1() {
        return word1;
    }

    public void setWord1(String word1) {
        this.word1 = word1;
    }

    public String getWord1Mean() {
        return word1Mean;
    }

    public void setWord1Mean(String word1Mean) {
        this.word1Mean = word1Mean;
    }
}
